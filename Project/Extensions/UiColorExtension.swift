import Foundation
import UIKit
extension UIColor {
    static var backgroundColor: UIColor {
        return UIColor(red:0.92, green:0.96, blue:0.98, alpha:1.0)
    }
    
    static var blueColor: UIColor {
        return UIColor(hex:"00ADEE")!
    }
}
